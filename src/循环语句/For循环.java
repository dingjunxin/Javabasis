package 循环语句;

public class For循环 {
    public static void main(String[] args) {
        /**
         * for（第一步)初始化表达式：（第二步）（第五步）循环条件表达式：（第四步）（第七步）循环后的操作表达式）
         * {
         * 执行语句（第三步）（第六步）
         * }
         */
        for (int i=0;i<3;i++){
            System.out.println("i="+i);
        }
    }
}
